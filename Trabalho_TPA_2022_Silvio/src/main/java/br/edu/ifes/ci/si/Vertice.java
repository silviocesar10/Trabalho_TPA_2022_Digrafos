package br.edu.ifes.ci.si;



/**
 *
 * @author silvio
 */
public class Vertice {
    private int vertice;
    private int autor;
   
    
    
    public Vertice(int vertice, int autor) {
        this.vertice = vertice;
        this.autor = autor;
    }

    
    public int getVertice() {
        return vertice;
    }

    public void setVertice(int vertice) {
        this.vertice = vertice;
    }

    public int getAutor() {
        return autor;
    }

    public void setAutor(int autor) {
        this.autor = autor;
    }
    
    
    
}
